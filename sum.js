
// sum with for loop

let sum =0;

for (let i =1; i<=20; i++) {
    sum =sum+i;
    console.log(i);
}
console.log ("sum of the number 1 to 20:", sum);

// sum with while loop

let numbers=1;
let total =0;
while (numbers<=20){
    console.log(numbers);
    total=total+numbers;
    console.log("sum of the numbers:", total);
    numbers++;
}

